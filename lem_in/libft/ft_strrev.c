#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		len;
	char	temp;

	if (!str)
		return (NULL);
	i = 0;
	len = ft_strlen(str);
	while (i < (len / 2))
	{
		temp = str[i];
		str[i] = str[len - i - 1];
		str[len - i - 1] = temp;
		i++;
	}
	return (str);
}
